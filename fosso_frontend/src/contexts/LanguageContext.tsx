const getNestedTranslation = (obj: Translations, path: string): string => {
  const keys = path.split(".");
  let current: any = obj;

  for (const key of keys) {
    if (current[key] === undefined) {
      console.warn(`Translation key not found: ${path}`);
      return path;
    }
    current = current[key];
  }

  if (typeof current !== "string") {
    console.warn(`Translation key is not a string: ${path}`);
    return path;
  }

  return current;
};

interface LanguageProviderProps {
  children: ReactNode;
}

export const LanguageProvider: React.FC<LanguageProviderProps> = ({
  children,
}) => {
  const [language, setLanguageState] = useState<SupportedLanguage>(() => {
    const savedLanguage = localStorage.getItem(
      "language"
    ) as SupportedLanguage | null;
    if (savedLanguage && Object.keys(translations).includes(savedLanguage)) {
      return savedLanguage;
    }

    const browserLang = navigator.language.split("-")[0] as SupportedLanguage;
    if (Object.keys(translations).includes(browserLang)) {
      return browserLang;
    }

    return "en";
  });

  useEffect(() => {
    localStorage.setItem("language", language);
    document.documentElement.setAttribute("lang", language);
  }, [language]);

  const setLanguage = (newLanguage: SupportedLanguage) => {
    setLanguageState(newLanguage);
  };

  const t = (
    key: string,
    args?: { [key: string]: string | number }
  ): string => {
    const translation = getNestedTranslation(translations[language], key);

    if (args) {
      return Object.entries(args).reduce((text, [argKey, argValue]) => {
        return text.replace(new RegExp(`{{${argKey}}}`, "g"), String(argValue));
      }, translation);
    }

    return translation;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};
